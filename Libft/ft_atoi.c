#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	s;
	int	r;
	int	i;

	s = 1;
	r = 0;
	i = 0;
	while (nptr[i] == 32 || (nptr[i] >= 9 && nptr[i] <= 13))
		i++;
	if (nptr[i] == '+' || nptr[i] == '-')
	{
		if (nptr[i] == '-')
			s = -1;
		i++;
	}
	while (nptr[i])
		r = r * 10 + (nptr[i++] - '0');
	return (r * s);
}
